public class Coordinate{

    int xpos;
    int ypos;

    public Coordinate(int x,int y){
        xpos = x;
        ypos = y;
    }

    public int getXpos() {
        return xpos;
    }

    public void setXpos(int xpos) {
        this.xpos = xpos;
    }

    public int getYpos() {
        return ypos;
    }

    public void setYpos(int ypos) {
        this.ypos = ypos;
    }

}